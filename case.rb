weather = 'sunny'
case weather
when 'sunny'
  puts "What a wonderful time to be alive! shorts it is!"
when 'raining'
  puts "Better not forget the umbrella"
when 'snowing'
  puts "it's time for lots of layers"
when 'cloudy'
  puts "Best to prepare for the worst, you didn't straighten your hair for no reason!"
end
